package sxc.Semester;

public class Semester {

	private int id;
	private int accYear;
	private int semType;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getaccYear() {
		return accYear;
	}

	public void setaccYear(int accYear) {
		this.accYear = accYear;
	}

	public int getsemType() {
		return semType;
	}

	public void setsemType(int semType) {
		this.semType = semType;
	}

}
